public class Encap {
    private String name;
    private int tc;
    private int age;
    

    public void setName(String name) {
        this.name = name;
    }

    public void setTc(int tc) {
        this.tc = tc;
    }

    public void setAge(int age) {
        this.age = age;
    }

    
    
    public String getName() {
        return name;
    }

    public int getTc() {
        return tc;
    }

    public int getAge() {
        return age;
    }
    
    
}
